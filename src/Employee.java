public class Employee {
    public int id;
    public String name;
    public String position;
    public String hireDate;
    public double salary;

    public Employee() {
    }

    public Employee(int id, String name, String position, String hireDate, double salary) {
        this.id = id;
        this.name = name;
        this.position = position;
        this.hireDate = hireDate;
        this.salary = salary;
    }
}

